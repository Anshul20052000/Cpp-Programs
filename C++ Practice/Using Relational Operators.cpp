#include<iostream>
using namespace std;
int main()
{
    int n;
    cout<<"Enter a Number : ";
    cin>>n;
    cout<<"\n ( n>10 ) : "<<(n > 10)<<endl;
    cout<<"\n ( n<10 ) : "<<(n < 10)<<endl;
    cout<<"\n ( n>=10 ) : "<<(n >= 10)<<endl;
    cout<<"\n ( n<=10 ) : "<<(n <= 10)<<endl;
    cout<<"\n ( n==10 ) : "<<(n == 10)<<endl;
    cout<<"\n ( n!=10 ) : "<<(n != 10)<<endl;
    return 0;
}

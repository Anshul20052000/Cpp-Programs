#include<iostream>
using namespace std;
int main()
{
    int a;
    char b;
    float c;
    double d;
    long e;
    bool f;
    cout<<"Size of Different data Types is : "<<endl;
    cout<<"Integer "<<sizeof(a)<<endl;
    cout<<"Character "<<sizeof(b)<<endl;
    cout<<"Float "<<sizeof(c)<<endl;
    cout<<"Double "<<sizeof(d)<<endl;
    cout<<"Long "<<sizeof(e)<<endl;
    cout<<"Boolean "<<sizeof(f)<<endl;
    return 0;
}
